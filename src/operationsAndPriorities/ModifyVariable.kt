package operationsAndPriorities

fun main() {

    var i = 5;
    println(i++)
    print(i--)

    var count = 1;
    println("Count: $count")

    count++
    println("Count: $count")

    count += 5
    println("Count: $count")

    count--
    println("Count: $count")

    count -= 2
    println("Count: $count")

}